## Paddle

- `protonemedia/laravel-paddle` provides a fluent wrapper around the Paddle.com API for payments, subscriptions, and webhooks in Laravel.
- Always activate the `paddle-development` skill when working with Paddle payments, subscriptions, webhooks, checkout links, or any code that uses the `Paddle` facade or Paddle event classes.
