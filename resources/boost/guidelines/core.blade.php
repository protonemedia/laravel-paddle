## Laravel Paddle

- `protonemedia/laravel-paddle` integrates the Paddle.com billing API with Laravel, providing a fluent API wrapper, webhook handling with signature verification, and a Blade directive for Paddle.js.
- Always activate the `laravel-paddle-development` skill when working with Paddle API calls, webhook events, pay links, subscription management, or any code that uses the `Paddle` facade or Paddle event classes.
